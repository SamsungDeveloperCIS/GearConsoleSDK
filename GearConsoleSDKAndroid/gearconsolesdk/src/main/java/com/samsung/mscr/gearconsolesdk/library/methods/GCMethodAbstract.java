package com.samsung.mscr.gearconsolesdk.library.com.samsung.mscr.gearconsolesdk.library.methods;

import android.content.Context;
import android.content.Intent;

import com.samsung.mscr.gearconsolesdk.library.GCUtils;
import com.thetransactioncompany.jsonrpc2.JSONRPC2Request;

import java.util.HashMap;
import java.util.Map;

public abstract class GCMethodAbstract< T extends GCMethodAbstract> {
    public String mMethodName;
    public int mId = 0;
    protected Map<String,Object> mParams;
    protected Context mContext;

    public GCMethodAbstract(Context context) {
        mContext = context;
        mParams = new HashMap<String, Object>();
    }

    /**
     * Set JSONRPC requestId
     * @param id
     * @return GCMethodAbstract
     */
    public T setId(int id) {
        this.mId = id;
        return (T)this;
    }

    /**
     * Send broadcast with json string to service
     * @return void
     */
    public void send() {
        Intent intent = new Intent(GCUtils.INTENT_NAME);
        intent.putExtra("raw", this.toString());
        mContext.sendBroadcast(intent);
    }

    /**
     * Returns json string
     * if requestId not set then will auto generated by GCUtils.getNextRequestId
     * @return String
     */
    public String toString() {
        if (mId == 0) {
            mId = GCUtils.getNextRequestId(mContext);
        }
        JSONRPC2Request reqOut = new JSONRPC2Request(this.mMethodName, mParams, mId);
        return reqOut.toString();
    }
}